<template lang="jade">
div.white.settings
  div.row
    div.input-field.center.opacity
      input#opacity_range.range.validate(type="range"
        v-model="opacity"
        @change="onChangeOpacityRange")
      label(for="opacity_range").label Opacity is {{ opacityFloor }}
      div.min-max.grey-text
        span.min 0
        span.max 100
  div.displays
    div.btn(v-for="(display, index) in settings.displays", @click="selectDisplay(index)")
      i.material-icons.left desktop_windows
      span {{ index + 1 }}
  div.row.center.resize
    div.btn(@click="resizePlayer")
      i.material-icons.left transform
      span resize player
  div.center.disruptive
    div.btn.blue(@click="reload")
      i.material-icons.left refresh
      span reload
    div.btn.red(@click="quit")
      i.material-icons.left close
      span quit
</template>
<script>
const ipc = require('renderer/ipc')
const types = require('root/mutation-types')

module.exports = {
  name: 'settings',
  data () {
    return {
      opacity: 5
    }
  },
  computed: {
    settings () {
      return this.$store.state.settings
    },
    opacityFloor () {
      return Math.floor(this.settings.player.opacity * 100)
    }
  },
  methods: {
    onChangeOpacityRange () {
      ipc.commit(types.CHANGE_OPACITY, this.$data.opacity / 100)
    },
    toggleClickThrough () {
      ipc.commit(types.CHANGE_THROUGTH)
    },
    quit () {
      ipc.commit(types.QUIT)
    },
    reload () {
      ipc.commit(types.RELOAD)
    },
    selectDisplay (display) {
      ipc.commit(types.SELECT_DISPLAY, display)
    },
    resizePlayer () {
      ipc.commit(types.RESIZE_PLAYER_MODE)
    }
  }
}
</script>
